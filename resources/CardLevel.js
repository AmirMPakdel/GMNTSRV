function level2cards(level){

  let cards = {
    nor:[],
    nil:[],
    sco:[],
    mon:[]
  }

  for(let i=0; i<=level; i++){
    
    card_level[i.toString()].forEach((v)=>{

      let c_o = getCard(v);
      if(c_o.position=="l"){

      }else if(c_o.faction =="neu"){
        cards["nor"].push(v);
        cards["nil"].push(v);
        cards["sco"].push(v);
        cards["mon"].push(v);
      }else{
        cards[c_o.faction].push(v);
      }
     
    });
  }

  return cards;
}

